package JavaBean;

public class MyMessBean {
	private String name;
	private String password;
	public MyMessBean(){
		
	}
	public String getName()
	{
		return name;
	}
	public void setName(String name){
		this.name = name;
	}
	public String getPassword()
	{
		return password;
	}
	public void setPassword(String password){
		this.password = password;
	}
}
